W bieżącym roku 2010 obchodzimy 31-tą rocznicę powstania naszego środowiska Wesoła Wiara i&nbsp;jego nieprzerwanej działalności od 1979r.<br/>
Z tej okazji Spotkanie Członków Wesołej Wiary powołało do życia Komitet Organizacyjny Obchodów Jubileuszu 31-lecia, który zajmuje się organizacją przedsięwzięć związanych z ta okazją.<br/>
<br/>
Zlot odbędzie się <span class="grubo">18 września 2010 w Przylepie na lotnisku</span>.
Zbiórka o godzinie <span class="grubo">o&nbsp;godz. 14.00</span>.
Na godzinę 15.00 planowany jest uroczysty apel.
Po apelu bedzie można kupić pamiątki Wesołej Wiary,
zjeść grochówkę i obejrzeć wystawę fotograficzną.
Na godzinę 19.00 planowane jest ognisko harcerskie z pokazem fotografii.
